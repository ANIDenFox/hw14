package Animal;

abstract class Pet implements Animal {
    private String name;
    private int age;

    public Pet(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    @Override
    public abstract void makeSound();

    @Override
    public abstract void eat();
}